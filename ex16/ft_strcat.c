char	*ft_strcat(char *s1, char *s2)
{
	char *s;

	s = s1;
	if (!*s1)
		return (s2);
	while (*s1)
		s1++;
	while (1)
	{
		if ((*s1++ = *s2++) == 0)
			break ;
	}
	*s1 = '\0';
	return (s);
}
