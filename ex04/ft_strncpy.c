char	*ft_strncpy(char *dst, char *src, unsigned int n)
{
	char *d;
	char *s;

	s = src;
	d = dst;
	if (n != 0)
	{
		while (n != 0)
		{
			if ((*d++ = *s++) == 0)
			{
				while (--n != 0)
					*d++ = 0;
				break ;
			}
			n--;
		}
	}
	return (dst);
}
