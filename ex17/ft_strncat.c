char	*ft_strncat(char *s1, char *s2, int n)
{
	int len;
	int i;

	i = 0;
	len = 0;
	while (s1[len])
		len++;
	while (i < n)
	{
		s1[len + i] = s2[i];
		i++;
	}
	s1[len + n] = '\0';
	return (s1);
}
