int		ft_str_is_alpha(char *s)
{
	int i;
	int ret;

	ret = 1;
	i = 0;
	while (s[i])
	{
		if (!((s[i] >= 'a' && s[i] <= 'z') || (s[i] >= 'A' && s[i] <= 'Z')))
			ret = 0;
		i++;
	}
	if (s[0] == '\0')
		ret = 1;
	return (ret);
}
