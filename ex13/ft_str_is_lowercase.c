int		ft_str_is_lowercase(char *s)
{
	int i;
	int ret;

	ret = 1;
	i = 0;
	while (s[i])
	{
		if (!(s[i] >= 'a' && s[i] <= 'z'))
			ret = 0;
		i++;
	}
	if (s[0] == '\0')
		ret = 1;
	return (ret);
}
